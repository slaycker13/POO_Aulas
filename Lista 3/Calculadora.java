public class Calculadora{
    public int soma(int valorA, int valorB){
        return valorA+valorB;
    }

    public int subtração(int valorA, int valorB){
        return valorA-valorB;
    }

    public int divisão(int valorA, int valorB){
        return valorA/valorB;
    }

    public int multiplicacao(int valorA, int valorB){
        return valorA*valorB;
    }

    public float soma(float valorA, float valorB){
        return valorA+valorB;
    }

    public float subtração(float valorA, float valorB){
        return valorA-valorB;
    }

    public float divisão(float valorA, float valorB){
        return valorA/valorB;
    }

    public float multiplicacao(float valorA, float valorB){
        return valorA*valorB;
    }
}